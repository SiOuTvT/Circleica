#!/bin/sh
set -e

C='\033[0;36m'
G='\033[0;32m'
Y='\033[1;33m'
R='\033[0;31m'
B='\033[1m'
N='\033[0m'

SECRET_FILE="/app/.secret"

# ── 启动横幅 ─────────────────────────
echo ""
printf "${C}${B}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${N}\n"
printf "${C}${B} Fangame 正在启动...${N}\n"
printf "${C}${B}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${N}\n"
echo ""

# ── 密钥管理（静默） ──────────────────
SECRET_AUTOGENERATED=false
if [ -z "$NEXTAUTH_SECRET" ]; then
  if [ -f "$SECRET_FILE" ]; then
    export NEXTAUTH_SECRET=$(cat "$SECRET_FILE")
  else
    NEW_SECRET=$(openssl rand -base64 48)
    echo "$NEW_SECRET" > "$SECRET_FILE"
    chmod 600 "$SECRET_FILE" 2>/dev/null || true
    export NEXTAUTH_SECRET="$NEW_SECRET"
    SECRET_AUTOGENERATED=true
  fi
fi

# ── 等待数据库就绪 ────────────────────
printf "  ⏳ 等待数据库就绪...\n"
MAX_WAIT=60
WAITED=0
while [ $WAITED -lt $MAX_WAIT ]; do
  DB_ERR=$(node -e "
    const { PrismaClient } = require('@prisma/client');
    const p = new PrismaClient({ datasources: { db: { url: process.env.DATABASE_URL } } });
    p.\$queryRaw\`SELECT 1\`.then(() => { process.exit(0); }).catch((e) => { console.error(e.message); process.exit(1); });
  " 2>&1) && break
  WAITED=$((WAITED + 2))
  if [ $WAITED -ge $MAX_WAIT ]; then
    printf "  ${R}✗${N} 数据库连接超时（${MAX_WAIT}秒）\n"
    echo "$DB_ERR"
    exit 1
  fi
  sleep 2
done
printf "  ${G}✓${N} 数据库已就绪\n"

# ── 数据库迁移 ───────────────────────
printf "  ⏳ 执行数据库迁移...\n"
MIGRATE_OUTPUT=$(node ./node_modules/prisma/build/index.js migrate deploy --schema=./prisma/schema.prisma 2>&1)
MIGRATE_EXIT=$?
if [ $MIGRATE_EXIT -eq 0 ]; then
  printf "  ${G}✓${N} 数据库迁移完成\n"
else
  printf "  ${R}✗${N} 数据库迁移失败 (exit code: ${MIGRATE_EXIT})\n"
  echo "--- 迁移错误详情 ---"
  echo "$MIGRATE_OUTPUT"
  echo "--- 结束 ---"
  exit 1
fi

# ── 状态面板 ─────────────────────────
APP_URL="${NEXTAUTH_URL:-http://localhost:3000}"
APP_VER="${APP_VERSION:-unknown}"

echo ""
printf "${C}${B}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${N}\n"
printf "${C}${B} Fangame v${APP_VER} 已启动成功${N}\n"
printf "${C}${B}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${N}\n"
echo ""
printf "  ${B}访问地址:${N} ${G}${APP_URL}${N}\n"
echo ""
printf "  ${B}NEXTAUTH_SECRET:${N} "
if [ "$SECRET_AUTOGENERATED" = true ]; then
  printf "${Y}⚠ 已自动生成${N}\n"
else
  printf "${G}✓ 已配置${N}\n"
fi

echo ""
printf "${C}${B}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${N}\n"
echo ""

export NODE_ENV=production
export NODE_OPTIONS="--max-old-space-size=512 --max-http-header-size=1048576"

exec node server.js
