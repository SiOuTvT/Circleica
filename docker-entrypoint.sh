#!/bin/sh
set -e

C='\033[0;36m'
G='\033[0;32m'
Y='\033[1;33m'
R='\033[0;31m'
B='\033[1m'
N='\033[0m'

SECRET_FILE="/app/.secret"

# ── 启动横幅 ─────────────────────────
echo ""
printf "${C}${B}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${N}\n"
printf "${C}${B} Fangame 正在启动...${N}\n"
printf "${C}${B}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${N}\n"
echo ""

# ── 密钥管理（静默） ──────────────────
SECRET_AUTOGENERATED=false
if [ -z "$NEXTAUTH_SECRET" ]; then
  if [ -f "$SECRET_FILE" ]; then
    export NEXTAUTH_SECRET=$(cat "$SECRET_FILE")
  else
    NEW_SECRET=$(openssl rand -base64 48)
    echo "$NEW_SECRET" > "$SECRET_FILE"
    chmod 600 "$SECRET_FILE" 2>/dev/null || true
    export NEXTAUTH_SECRET="$NEW_SECRET"
    SECRET_AUTOGENERATED=true
  fi
fi

# ── 数据库迁移 ────────────────────────
# 策略：先检测是否有遗留的失败迁移，自动修复后再执行正常迁移
MIGRATE_OK=false

fix_failed_migrations() {
  # 查找 _prisma_migrations 中所有 rolled_back_count > 0 的记录
  FAILED=$(
    node -e "
      const { PrismaClient } = require('@prisma/client');
      const p = new PrismaClient();
      (async () => {
        try {
          const rows = await p.\$queryRaw\`
            SELECT migration_name, rolled_back_count
            FROM _prisma_migrations
            WHERE rolled_back_count > 0
            ORDER BY started_at
          \`;
          console.log(JSON.stringify(rows));
        } catch(e) {
          // 表不存在，说明是全新数据库
          console.log('[]');
        }
        await p.\$disconnect();
      })();
    " 2>/dev/null
  )

  if [ -z "$FAILED" ] || [ "$FAILED" = "[]" ]; then
    return 0
  fi

  printf "  ${Y}⚠${N} 检测到失败的迁移记录，正在修复...\n"

  # 用 awk 加手动循环提取 migration_name —— 比 jq 更可靠（镜像没装 jq）
  NAMES=$(echo "$FAILED" | node -e "
    const raw = require('fs').readFileSync('/dev/stdin', 'utf8').trim();
    if (!raw || raw === '[]') { process.exit(0); }
    const arr = JSON.parse(raw);
    for (const r of arr) {
      console.log(r.migration_name);
    }
  " 2>/dev/null)

  if [ -z "$NAMES" ]; then
    return 0
  fi

  for NAME in $NAMES; do
    printf "    ↪ resolve: ${NAME}\n"
    npx prisma migrate resolve --rolled-back "$NAME" --schema=./prisma/schema.prisma 2>&1 || true
  done

  printf "  ${G}✓${N} 失败迁移已修复\n"
}

for RETRY in $(seq 0 10); do
  # 第一步：修复历史失败记录
  fix_failed_migrations

  # 第二步：执行正常迁移
  printf "  ⏳ 执行数据库迁移 (${RETRY}/10)...\n"
  MIGRATE_OUTPUT=$(npx prisma migrate deploy --schema=./prisma/schema.prisma 2>&1)
  MIGRATE_EXIT=$?

  if [ $MIGRATE_EXIT -eq 0 ]; then
    MIGRATE_OK=true
    printf "  ${G}✓${N} 数据库迁移完成\n"
    break
  fi

  # 打印失败原因（便于调试）
  printf "  ${Y}⚠${N} 迁移失败:\n"
  echo "$MIGRATE_OUTPUT" | while IFS= read -r line; do printf "    %s\n" "$line"; done

  if [ $RETRY -lt 10 ]; then
    printf "  ${Y}⏳${N} 等待重试...\n"
    sleep 3
  fi
done

if [ "$MIGRATE_OK" != true ]; then
  printf "  ${R}✗${N} 数据库迁移彻底失败\n"
  exit 1
fi

# ── 状态面板 ─────────────────────────
APP_URL="${NEXTAUTH_URL:-http://localhost:3000}"
APP_VER="${APP_VERSION:-unknown}"

echo ""
printf "${C}${B}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${N}\n"
printf "${C}${B} Fangame v${APP_VER} 已启动成功${N}\n"
printf "${C}${B}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${N}\n"
echo ""
printf "  ${B}访问地址:${N} ${G}${APP_URL}${N}\n"
echo ""
printf "  ${B}NEXTAUTH_SECRET:${N} "
if [ "$SECRET_AUTOGENERATED" = true ]; then
  printf "${Y}⚠ 已自动生成${N}\n"
else
  printf "${G}✓ 已配置${N}\n"
fi

echo ""
printf "${C}${B}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${N}\n"
echo ""

export NODE_ENV=production
export NODE_OPTIONS="--max-old-space-size=512 --max-http-header-size=1048576"

exec node server.js